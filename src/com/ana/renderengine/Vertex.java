package com.ana.renderengine;

/**
 * This class represents a point in 3D space with x, y, and z coordinates.
 * X coordinate represents left-right movement
 * Y coordinate represents up-down movement
 * Z coordinate means depth; positive Z = moving towards the observer
 */
public class Vertex {
    private double x;
    private double y;
    private double z;

    public Vertex(double x, double y, double z) {
        this.x = x;
        this.y = y;
        this.z = z;
    }

    public double getX() {
        return x;
    }

    public double getY() {
        return y;
    }

    public double getZ() {
        return z;
    }

    public void setX(double x) {
        this.x = x;
    }

    public void setY(double y) {
        this.y = y;
    }

    public void setZ(double z) {
        this.z = z;
    }
}
